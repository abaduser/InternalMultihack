/*
================
Q_floatIsNan

Don't pass doubles to this
================
*/
int Q_floatIsNan (float x)
{
	floatint_t fi;

	fi.f = x;
	fi.ui &= 0x7FFFFFFF;
	fi.ui = 0x7F800000 - fi.ui;

	return (int)( (unsigned int)fi.ui >> 31 );
}
